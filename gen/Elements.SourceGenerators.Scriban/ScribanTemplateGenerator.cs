    private static class DiagnosticDescriptors
    {
        private const string Template = nameof(Template);
        private const string TemplateCategory = nameof(Template);

        internal static readonly Func<Exception, DiagnosticDescriptor> ConfigurationReadError = (e) => new("ESBN0001", Localizables.Titles.ConfigurationError([e.GetType().Name]), Localizables.Messages.ConfigurationError([e.Message]), "Configuration", DiagnosticSeverity.Error, true, "Indicates a configuration read error");

        internal static readonly DiagnosticDescriptor EmptyTemplateError = new ("ESBN0002", Localizables.Titles.EmptyTemplate, Localizables.Messages.EmptyTemplate, TemplateCategory, DiagnosticSeverity.Error, true, "The template is empty."); 

        internal static readonly DiagnosticDescriptor AutoGeneratedHintMissing = new ("ESNB0003", Localizables.Titles.AutoGeneratedHintMissing, Localizables.Messages.AutoGeneratedHintMissing, TemplateCategory, DiagnosticSeverity.Warning, true, "Add a comment with <auto-generate /> to the beginning of the template.");
        internal static readonly DiagnosticDescriptor GeneratedCodeMissing = new ("ESNB0004", Localizables.Titles.GeneratedCodeAttributeMissing, Localizables.Messages.GeneratedCodeAttributeMissing, TemplateCategory, DiagnosticSeverity.Warning, true, "Add [GeneratedCode] attribute to your template.");
        internal static readonly DiagnosticDescriptor ExcludeFromCodeCoverageMissing = new ("ESNB0005", Localizables.Titles.ExcludeFromCodeCoverageAttributeMissing, Localizables.Messages.ExcludeFromCodeCoverageAttributeMissing, TemplateCategory, DiagnosticSeverity.Info, true, "Add [ExcludeFromCodeCoverage] attribute to your template.");
    }
    
    private const string ScribanCSharpTemplateFileExtension = ".scriban-cs";

    private const string ScribanCSharpConfigFileExtension = ".json-cs";

    private const string GeneratedFileExtension = ".g.cs";

    public void Initialize(IncrementalGeneratorInitializationContext context)
    {
        IncrementalValuesProvider<AdditionalText> configFileProviders = context.AdditionalTextsProvider.Where(
            f => f.Path.EndsWith(ScribanCSharpConfigFileExtension, StringComparison.OrdinalIgnoreCase)
        );
        IncrementalValuesProvider<AdditionalText> templateFileProvider = context.AdditionalTextsProvider.Where(
            f => f.Path.EndsWith(ScribanCSharpTemplateFileExtension, StringComparison.OrdinalIgnoreCase)
        );

        IncrementalValueProvider<ImmutableArray<AdditionalText>> configFiles = configFileProviders.Collect();
        IncrementalValueProvider<ImmutableArray<AdditionalText>> templateFiles = templateFileProvider.Collect();

        IncrementalValueProvider<(ImmutableArray<AdditionalText> Left, ImmutableArray<AdditionalText> Right)> combinedFiles = configFiles.Combine(templateFiles);

        IncrementalValueProvider<ImmutableArray<(AdditionalText Config, AdditionalText Template)>> matchedPairs = combinedFiles.Select(static (tuple, _) =>
        {
            var (configs, templates) = tuple;
            return configs.Join(
                templates,
                file => AdditionalTextToName(file),
                file => AdditionalTextToName(file),
                (config, template) => (Config: config, Template: template)
            ).ToImmutableArray();
        });

        context.RegisterSourceOutput(matchedPairs, this.GenerateSourceForScribanCsFiles);

        static string AdditionalTextToName(AdditionalText fileDescriptor)
        {
            return Path.GetFileNameWithoutExtension(fileDescriptor.Path);
        }
    }

    private void GenerateSourceForScribanCsFiles(SourceProductionContext context, ImmutableArray<(AdditionalText, AdditionalText)> sourcesAndConfigs)
    {
        foreach(var sourceAndConfig in sourcesAndConfigs)
        {
            (AdditionalText source, AdditionalText config) = sourceAndConfig;
            GenerateSourceForScribanCsFile(context, source, config);
        }
    }

    private static void GenerateSourceForScribanCsFile(SourceProductionContext context, AdditionalText source, AdditionalText config)
    {
        (JsonElement modelNode, Exception? error) = ReadFile(config, context.CancellationToken);
        if (error is not null)
        {
            context.ReportDiagnostic(Diagnostic.Create(DiagnosticDescriptors.ConfigurationReadError(error), GetLocation(config), error));
            return;
        }
    
        SourceText? templateText = source.GetText(context.CancellationToken);
        if (templateText is null)
        {
            context.ReportDiagnostic(Diagnostic.Create(DiagnosticDescriptors.EmptyTemplateError, GetLocation(source), error));
            return;
        }

        Template scribanTemplate = Template.Parse(templateText.ToString(), source.Path, ParserOptions.Default, LexerOptions.Default);
        string renderedText = scribanTemplate.Render(modelNode);
        CheckRenderedText(renderedText, context, GetLocation(source));
        string reRenderedText = ReRenderTextWithCodeGeneratorAttributed(renderedText, source.Path, config.Path, modelNode);

        SourceText sourceText = SourceText.From(reRenderedText);
        
        string fileName = Path.GetFileNameWithoutExtension(source.Path);
        fileName = $"{fileName}{GeneratedFileExtension}";
        context.AddSource(fileName, sourceText);
    }

    private static Location GetLocation(AdditionalText descriptor)
    {
        return Location.Create(descriptor.Path, TextSpan.FromBounds(0, 0), new LinePositionSpan());
    }

    private static void CheckRenderedText(string renderedText, SourceProductionContext context, Location location)
    {
        if (!Contains(renderedText, "<auto-generated"))
        {
            context.ReportDiagnostic(Diagnostic.Create(DiagnosticDescriptors.AutoGeneratedHintMissing, location));
        }

        if (!Contains(renderedText, "GeneratedCode("))
        {
            context.ReportDiagnostic(Diagnostic.Create(DiagnosticDescriptors.GeneratedCodeMissing, location));
        }

        if (!Contains(renderedText, "ExcludeFromCodeCoverage(", "ExcludeFromCodeCoverage]"))
        {
            context.ReportDiagnostic(Diagnostic.Create(DiagnosticDescriptors.ExcludeFromCodeCoverageMissing, location));
        }

        static bool Contains(string haystack, params string[] needles)
        {
            return needles.Any(n => haystack.IndexOf(n, StringComparison.OrdinalIgnoreCase) >= 0);
        }
    }

    private static string ReRenderTextWithCodeGeneratorAttributed(string renderedText, string templateFilePath, string configFilePath, JsonElement modelNode)
    {
        StringBuilder buffer = new();

        buffer.AppendLine("/*");

        AppendHeaderCommentToBuffer(buffer, "SourceGenerator", typeof(ScribanTemplateGenerator).FullName ?? nameof(ScribanTemplateGenerator));
        AppendHeaderCommentToBuffer(buffer, "GeneratorVersion", typeof(ScribanTemplateGenerator).Assembly.GetCustomAttribute<AssemblyFileVersionAttribute>()?.Version ?? "<undefined>");
        AppendHeaderCommentToBuffer(buffer, "ScribanFile", templateFilePath);
        AppendHeaderCommentToBuffer(buffer, "ConfigFile", configFilePath);
        
        IReadOnlyDictionary<string, string> nodes = DumpModelNode(modelNode);
        foreach (KeyValuePair<string, string> kvp in nodes)
        {
            AppendHeaderCommentToBuffer(buffer, $"config.{kvp.Key}", kvp.Value);
        }

        buffer.AppendLine(" */");


        buffer.Append(renderedText);

        return buffer.ToString();

        static void AppendHeaderCommentToBuffer(StringBuilder builder, string field, string value)
        {
            const char WhiteSpace = ' ';
            const char Colon = ':';
            const int LineLength = 80;
            const int FieldLineLength = (LineLength - 2) / 2;
            builder
                .Append(WhiteSpace)
                .Append(field)
                .Append(Colon);
            int padding = FieldLineLength - field.Length;
            if (padding > 0)
            {
                builder.Append(WhiteSpace, padding);
            }

            builder.Append(WhiteSpace)
                .Append(value)
                .AppendLine();
        }
    }

    private static IReadOnlyDictionary<string, string> DumpModelNode(JsonElement modelNode)
    {
        Dictionary<string, string> result = new();

        DumpNodeRecursive(modelNode, result, string.Empty);

        return result;

        static void DumpNodeRecursive(JsonElement currentNode, IDictionary<string, string> items, string prefix)
        {

            switch (currentNode.ValueKind)
            {
                case JsonValueKind.Array:
                    {
                        int counter = 0;
                        foreach (JsonElement node in currentNode.EnumerateArray())
                        {
                            DumpNodeRecursive(node, items, $"{prefix}[{counter}]");
                            counter++;
                        }
                        break;
                    }
                case JsonValueKind.Object:
                    {
                        foreach (JsonProperty node in currentNode.EnumerateObject())
                        {
                            DumpNodeRecursive(node.Value, items, "${prefix}.{node.Name}");
                        }
                        break;
                    }
                default:
                    {
                        string value = prefix;
                        if (!string.IsNullOrWhiteSpace(value))
                        {
                            value = nameof(value);
                        }
                        
                        items[value] = currentNode.ToString();
                        break;
                    }
            }
        }    
    }

    private static (JsonElement, Exception?) ReadFile(AdditionalText configFile, CancellationToken cancellationToken = default)
    {
        try
        {
            string text  =  configFile.GetText(cancellationToken)?.ToString() ?? throw new ArgumentException($"{configFile.Path} could not be read.");

            ReadOnlyMemory<char> readerText = text.AsMemory();
            JsonDocumentOptions options = new()
            {
                AllowDuplicateProperties = false,
                AllowTrailingCommas = true,
                CommentHandling = JsonCommentHandling.Allow,
                MaxDepth = 0,
            };
            JsonDocument document = JsonDocument.Parse(readerText, options);
            return (document.RootElement.Clone(), default);
        }
        catch (Exception e) when (e is 
            JsonException or 
            ArgumentException)
        {
            return (new JsonElement(), e);
        }
    }
}